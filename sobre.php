	Seja bem vindo ao Estacionamento da ETEC Fernando Prestes !
	</br></br>
	Um pouco da história da ETEC
	</br></br>
  A “Fernando Prestes” foi criada pela Lei 1860, de 30.12.1921 e instalada em 09 de junho de 1929, 
  com a denominação de “Escola Profissional Secundária Mista de Sorocaba”, atualmente Escola Técnica Estadual “Fernando Prestes”. 
  Teve como primeiros cursos implantados os de artes domésticas, 
  corte e confecção, bordado flores e chapéus, tornearia, entalhe em madeira, marcenaria, fundição, serralheria e 
  em regime de cooperação com a Estrada de Ferro Sorocabana o curso ferroviário.
  Em 1931 passa a ser denominada Escola Profissional Mista Cel. 
  Fernando Prestes.
  Ao longo dos anos muitas reformulações aconteceram em função das legislações e de mudanças de prédios. 
  “E em 1948 era feita a mudança para o amplo prédio agora próprio, e bem conhecido em nossos dias, à Av. Pereira Inácio”
  atualmente onde está localizada a Escola Técnica Estadual “Rubens de Faria e Souza”, oriunda de reformulações,
  tendo como origem a Escola “Fernando Prestes”.
  No final da década de sessenta é desalojada de seu prédio próprio para dar lugar ao Colégio Técnico Industrial Prof. Rubens de Faria e Souza, 
  com cursos técnicos de 2º grau e passa a funcionar como Ginásio Industrial “Fernando Prestes” no atual campus Seminário da Universidade de Sorocaba – UNISO.
  Novas mudanças aconteceriam, passando a ser a partir de 1982, em novo prédio,
  o atual, à Rua Natal, 340, Jardim Paulistano, o Centro Estadual Interescolar “Fernando Prestes”. 
  Uma experiência que não deu certo, pelo menos em Sorocaba. 
  O Centro Interescolar era uma escola de pré-profissionalização que oferecia cursos para os alunos da rede pública
  e privada que cursavam o ensino de 1º e 2º graus e vinham ali em horário diferentes dos seus cursos regulares para ter uma iniciação para o trabalho. 
  No caso do “Fernando Prestes” também funcionavam os cursos de 1º grau e os cursos técnicos de 2º grau.
  Outra importante mudança que ocorre em 1982, o Centro Estadual de Educação Tecnológica “Paula Souza” assume os cursos de técnicos de 2º grau da escola, deixando o 1º grau subordinado à secretaria de Educação 
  e a escola passa a ser denominar Escola Técnica Estadual “Fernando Prestes”.
  “No decorrer de sua ‘vida', a ETE ‘Fernando Prestes' ofereceu aos seus alunos cursos industriais, ferroviários, os conveniados com o MEC, o ginásio industrial e o técnico de 2º grau” (Cruzeiro do Sul, 1989: 32).
  A partir de 2006 o Centro Paula Souza altera a sigla de todas as escolas técnicas para “ETec” e assim a escola passa-se a chamar ETec Fernando Prestes.
  Atualmente mantém as habilitações profissionais de Técnico em Administração, Técnico em Desenvolvimento de Sistemas, Técnico em Informática para Internet, Técnico em Secretariado, 
  Técnico em Design de Interiores, Técnico em Segurança do Trabalho, 
  Técnico em Contabilidade, Técnico em Logística, Técnico em Agenciamento de Viagem, 
  Técnico em Finanças, Técnico em Edificações e Técnico em Eventos Integrado ao Ensino Médio, 
  Técnico em Desenvolvimento de Sistemas e Informática para Internet Integrado ao Ensino Médio, Técnico em Edificações Integrado ao Ensino Médio.
  A escola também mantém classes descentralizadas que funcionam em parceria com escola da rede estadual (Joaquim Izidoro Marins) onde funcionam o curso Técnico em Logística, Técnico em Informática e Administração, 
  e na cidade de Araçoiaba da Serra (na Escola Prof. Osmar Giacomelli), onde funciona o Curso Técnico em Administração, todas elas atendendo a LDB 9394/96, ao Decreto 5154/04.
  Oferece ainda, desde 1998, o Ensino Médio regular independente do Ensino Técnico, para os alunos oriundo do Ensino Fundamental (antigo 1° grau).
  Contando com aproximadamente 2800 alunos matriculados no primeiro semestre de 2015, o ensino técnico da “Fernando Prestes”, atende a uma clientela heterogênea, composta por adolescentes, 
  jovens e adultos, egressos do ensino médio (antigo 2º grau), cursando a segunda ou terceira série do mesmo, ou ainda, oriundos do terceiro grau que desejam sua reconversão profissional.
  Distribuídos nos diversos cursos, nos períodos matutino, vespertino e noturno, a escola atende a um público oriundo de escolas pública em sua grande maioria 84%, e o restante de escolas particulares,
  sendo que 49% dos alunos matriculados nos cursos técnicos na escola já estão inseridos no mercado de trabalho e vêm em busca de uma formação técnica .
  A escola tem uma área de abrangência que se estende por diversos bairros da cidade, servindo também municípios circunvizinhos como
  São Roque, Piedade, Ibiúna, Itu, Salto de Pirapora, Araçoiaba, Alumínio, Porto Feliz, Pilar do Sul, Boituva, Mairinque, Salto, Votorantim e Iperó.
  Ao longo do tempo a escola passou por diversas transformações, 
  resultantes das mudanças de legislação, de estrutura e de prédios. 
  Muitos profissionais se formaram na Escola Técnica, o espírito de pioneirismo e de dedicação de muitos que aqui trabalharam, acreditamos,
  vive até hoje no “Fernando Prestes”. <h6>